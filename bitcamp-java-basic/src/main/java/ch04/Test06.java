// 관계 연산자(<, <=, >, >=), 등위 연산자(==, !=)
package ch04;

public class Test06 {
  public static void main(String[] args) {
    System.out.println(10 < 20);
    System.out.println(10 <= 10);
    System.out.println(10 > 20);
    System.out.println(10 >= 10);
    System.out.println(10 == 20);
    System.out.println(10 != 20);
  }
}

















