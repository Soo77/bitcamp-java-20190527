// 부동소수점 리터럴의 크기
package ch02;

public class Test05 {
  public static void main(String[] args) {
    // 부동소수점 9876.54345678 을  출력하라
    System.out.println(9876.54345678); // => 9876.54345678
    
    // 부동소수점 9876.54345678f 출력하라
    System.out.println(9876.54345678f); // => 9876.543
  }
}

